const typeDefs = `
  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Book {
    title: String!
    published: String!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    bookCount: Int
    born: String
    id: ID!
  }

  type Mutation {
    addBook(
        title: String!
        published: String!
        author: String!
        genres: [String!]!
    ): Book
    editAuthor(
        name: String!
        setBornTo: String!
    ): Author
  }
`

const resolvers = {
  Query: {
    bookCount: async () => Books.collection.countDocuments(),
    authorCount: async () => Authors.collection.countDocuments(),
    allBooks: async (root, args) => {
        return Books.find({})
    },
    allAuthors: async ()=>{return Authors.find({})}
  },
  Author: {
    bookCount: (root)=>{
        return books.filter(b=>b.author===root.name).length
    }
  },
  Mutation: {
    addBook: async (root, args)=>{
        if(!authors.find(a=>a.name===args.author)){
            const newAuthor=Authors({name: args.author})
            await newAuthor.save()
        }
        const newBook=Books({...args})
        await newBook.save()
        return newBook
    },
    editAuthor: (root, args)=>{
        const author=authors.find(a=>a.name===args.name)
        if(!author){return null}
        const editedAuthor={...author, born: args.setBornTo}
        authors=authors.map(a=>a.name===author.name ? editedAuthor : a)
        return editedAuthor
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
